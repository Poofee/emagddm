/**
 * @file math_constants.hpp
 * @brief 数值计算层 - 数学与物理常量统一定义
 * @details 集中管理电磁有限元仿真中常用的数学常数和物理常量，
 *          确保全项目使用一致的常量值，避免硬编码分散和跨平台差异。
 *
 *          包含内容：
 *          - 数学常数：圆周率 π
 *          - 电磁物理常量：真空磁导率 μ₀、真空介电常数 ε₀、真空光速 c
 *
 * @note 所有常量均使用 constexpr 定义，编译期求值，零运行时开销
 * @note 跨平台兼容：MSVC 不默认定义 POSIX 的 M_PI 宏，本头文件统一提供
 *
 * @author Poofee
 * @date 2026-04-07
 * @version 1.0
 */

#pragma once

namespace numeric {

// ==================== 数学常数 ====================

/**
 * @brief 圆周率 π（高精度双精度值）
 * @details 值: 3.14159265358979323846...
 *          用于角度-弧度转换、极坐标/柱坐标积分因子（2πr）等场景
 */
constexpr double PI = 3.14159265358979323846;

// ==================== 电磁物理常量（SI单位制）====================

/**
 * @brief 真空磁导率 μ₀（精确定义值）
 * @details 值: 4π × 10⁻⁷ H/m ≈ 1.256637061435917...×10⁻⁶ H/m
 *          SI 基本物理常量，2019 年重新定义后为精确值
 *          用于静磁场、涡流场等涉及磁导率的材料默认参数设置
 */
constexpr double MU0 = 4.0 * PI * 1e-7;

/**
 * @brief 真空介电常数 ε₀（由 μ₀ 和光速 c 导出）
 * @details 值: 8.8541878128...×10⁻¹² F/m
 *          推导关系: ε₀ = 1 / (μ₀ · c²)
 *          用于静电场、瞬态电磁场等涉及介电常数的材料默认参数设置
 *
 * @note 此处取 CODATA 2018 推荐值的前几位有效数字，满足工程仿真精度要求
 */
constexpr double EPSILON0 = 8.854187817e-12;

/**
 * @brief 真空中的光速 c（精确定义值）
 * @details 值: 299792458 m/s（SI 精确定义）
 *          用于波数计算、时谐场角频率与波长转换等
 *          关系式: ω = c · k, λ = c / f
 */
constexpr double C_LIGHT = 299792458.0;

} // namespace numeric
